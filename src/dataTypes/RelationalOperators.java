package dataTypes;

public class RelationalOperators {
	public static void main(String[] args) {
		
		boolean result;
		
		result = (5 == 5);
			System.out.println("This is the result: "+ result);
			
		result = ( 10!=5 );
			System.out.println("This is the result: " +result);
			
		result = ( 10>8 );
			System.out.println("This is the result: "+ result);
			
		result = ( 4>=5 );
			System.out.println("This is the result: "+ result);
			
		result = ( 5<9 );
			System.out.println("This is the result: "+ result);
			
		result =( 5<=7 );
			System.out.println("This is the result: "+ result);
	}
}
