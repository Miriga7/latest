package dataTypes;

public class WhileLoop {

	public static void main(String[] args) {
		int a =0, Sum = 0;
		
		while (a<10); {
			System.out.println("The value of a" + a);
			
			Sum = Sum + a;
			a++;
				
		}
			System.out.println("The sum of numbers from 0 to 9" + Sum);
	} 

}
